    <script>
        document.addEventListener('DOMContentLoaded', function () {
            const passwordInput = document.getElementById('password');
            const togglePasswordButton = document.getElementById('togglePassword');
            const eyeIcon = document.getElementById('eyeIcon');

            // SVG do Olho Fechado
            const eyeClosedPath = `<path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M13.875 18.825A10.05 10.05 0 0112 19c-4.478 0-8.268-2.943-9.542-7a10.025 10.025 0 014.132-5.411m0 0L21 21M17.94 17.94A10.07 10.07 0 0021.542 12c-1.274-4.057-5.064-7-9.542-7-1.173 0-2.29.205-3.328.58m4.328 4.328a3 3 0 11-4.243 4.243" />`;
            
